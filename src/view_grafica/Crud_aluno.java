    public void popupaTabela(){
        FacadeAlunoController alunoController = new ManterAlunoController();
        ArrayList<AlunoTransferObject> alunos = alunoController.retornaAlunos();
        
        
        DefaultTableModel model = (DefaultTableModel) table_aluno.getModel();
        model.setRowCount(0);
        Object rowData[] = new Object[2];
        for (int i = 0; i < alunos.size(); i++) {
            rowData[0] = alunos.get(i).getNome();
            rowData[1] = alunos.get(i).getRg();
            model.addRow(rowData);
            
        }
    }

