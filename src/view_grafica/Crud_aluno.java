    private void btn_apagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_apagarActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) table_aluno.getModel();
        
        int row = table_aluno.getSelectedRow();
        System.out.println(row);
                
        
        String selected = model.getValueAt(row, 1).toString();
        
        if (row >= 0){
            try {
                FacadeAlunoController alunoController = new ManterAlunoController();
                alunoController.deletar_aluno(selected);
                System.out.println(selected);
                popupaTabela();
                
            } catch (Exception e) {
               JOptionPane.showMessageDialog(this, "Error");
            }
            
        }
        
        
        
    }//GEN-LAST:event_btn_apagarActionPerformed

    private void btn_atualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_atualizarActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) table_aluno.getModel();
        
        int row = table_aluno.getSelectedRow();
        System.out.println(row);
        
        String nome = model.getValueAt(row,0).toString();
        System.out.println(nome);        
        String rg = model.getValueAt(row, 1).toString();
        System.out.println(rg);
        
        if (row >= 0) {
            try {
                FacadeAlunoController alunoController = new ManterAlunoController();
                AlunoTransferObject alunoAtualizado = new AlunoTransferObject();
                alunoAtualizado.setNome(nome);
                alunoAtualizado.setRg(rg);
                alunoController.editar_aluno(rg, alunoAtualizado);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error");
            }
        }
        
        
    }//GEN-LAST:event_btn_atualizarActionPerformed

    private void btn_inserirLinhaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_inserirLinhaActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) table_aluno.getModel();
        FacadeAlunoController alunoController = new ManterAlunoController();
        
        AlunoTransferObject alunoTransferObject = new AlunoTransferObject();
        
        alunoTransferObject.setNome(txt_nome.getText());
        alunoTransferObject.setRg(txt_rg.getText());
        
        try {
            alunoController.cadastrar_aluno(alunoTransferObject);
            popupaTabela();            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error");
        }
         
        
       
  

       
    }//GEN-LAST:event_btn_inserirLinhaActionPerformed

