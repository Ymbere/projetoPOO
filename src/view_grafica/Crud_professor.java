    private void btn_apagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_apagarActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) table_professor.getModel();
        
        int row = table_professor.getSelectedRow();
        System.out.println(row);
        
        String selected = model.getValueAt(row, 1).toString();
        
        if (row >=0) {
            try {
                FacadeProfessorController professorController = new ManterProfessorController();
                professorController.deletaProfessor(selected);
                System.out.println(selected);
                popupaTabela();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error");
            }
            
        }
        
    }//GEN-LAST:event_btn_apagarActionPerformed

    private void btn_atualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_atualizarActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) table_professor.getModel();
        
        int row = table_professor.getSelectedRow();
        System.out.println(row);
        
        String nome = model.getValueAt(row,0).toString();
        System.out.println(nome);        
        String rg = model.getValueAt(row, 1).toString();
        System.out.println(rg);
        String titulo = model.getValueAt(row, 2).toString();
        System.out.println(titulo);
        
        if (row >= 0) {
            try {
                FacadeProfessorController professorController = new ManterProfessorController();
                ProfessorTransferObject professorAtualizado = new ProfessorTransferObject();
                professorAtualizado.setNome(nome);
                professorAtualizado.setRg(rg);
                professorAtualizado.setTitulo(titulo);
                professorController.editaProfessor(rg, professorAtualizado);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error");
            }
        }
                
    }//GEN-LAST:event_btn_atualizarActionPerformed

    private void btn_inserirLinhaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_inserirLinhaActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) table_professor.getModel();
        FacadeProfessorController professorController = new ManterProfessorController();
        
        ProfessorTransferObject professorTransferObject = new ProfessorTransferObject();
        
        professorTransferObject.setNome(txt_nome.getText());
        professorTransferObject.setRg(txt_rg.getText());
        professorTransferObject.setTitulo(txt_titulo.getText());
        
        try {
            professorController.cadastratProfessor(professorTransferObject);
            popupaTabela();            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error");
        }        
    }//GEN-LAST:event_btn_inserirLinhaActionPerformed

