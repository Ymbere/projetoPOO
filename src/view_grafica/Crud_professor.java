    public void popupaTabela(){
        FacadeProfessorController professorController = new ManterProfessorController();
        ArrayList<ProfessorTransferObject> professores = professorController.retornaProfessores();
        
        
        DefaultTableModel model = (DefaultTableModel) table_professor.getModel();
        model.setRowCount(0);
        Object rowData[] = new Object[3];
        for (int i = 0; i < professores.size(); i++) {
            rowData[0] = professores.get(i).getNome();
            rowData[1] = professores.get(i).getRg();
            rowData[2] = professores.get(i).getTitulo();
            model.addRow(rowData);            
        }
    }   

